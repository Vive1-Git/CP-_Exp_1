'''Creating module that contains functions for mathematical operations'''

def add(a,b):
    return a+b

def subtract(a,b):
    return a-b

def multiply(a,b):
    return a*b

def divide(a,b):
    return a/b

def modulus(a,b):
    return a%b

def square(a):
    return a**2

def square_root(a):
    return a**(1/2)
